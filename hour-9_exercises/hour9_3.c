#include <stdio.h>
/**
 * main - program to print the result of two numbers multiplied by other numbers
 *
 *The input numbers will be positive signed integers and the output will be a long int
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	long int a, b;

	a = 360;
	b = 180;

	printf("The first is : %ld \n", a * 240);
	printf("The second is : %ld \n", b * 550);

	return (0);
}
